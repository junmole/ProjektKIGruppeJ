class GameData {
    /**
     * String representing game state
     */
    String board;
    /**
     * Indicates if it is currently player 1's turn
     */
    boolean player1;
    /**
     * Indicates if it is currently player 2's turn
     */
    boolean player2;
    /**
     * Indicates if both players are connected to the server
     */
    boolean bothConnected;
}
